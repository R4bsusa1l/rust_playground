pub struct Guess {
    value: i32,
}

// Guess struct as a newtype pattern with (contract) failing if the value is not between 1 and 100
impl Guess {
    pub fn new(value: i32) -> Guess {
        if value < 1 || value > 100 {
            panic!("Guess value must be between 1 and 100, got {value}.");
        }

        Guess { value }
    }

    pub fn value(&self) -> i32 {
        self.value
    }
}
